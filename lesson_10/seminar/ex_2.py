class Rectangle:

    def __init__(self, length: int, width: int = None):
        self.length = length
        if width:
            self.width = width
        else:
            self.width = length

    def __eq__(self, other):
        return self.perimetr() == other.perimetr()

    def perimetr(self):
        return (self.length + self.width) * 2

    def square(self):
        return self.length * self.width


