class Info:
    def __init__(self, fio: str, age: int):
        self.fio = fio.title()
        self.__age = age

    def birthday(self):
        self.__age += 1

    def get_age(self):
        return self.__age

    def __str__(self):
        return f"{self.fio.title()}"

